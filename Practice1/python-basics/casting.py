x = int("10")      # string → int
print(x)
print(type(x))


y = float("3.14")  # string → float
print(y)
print(type(y))

z = str(123)       # int → string
print(z)
print(type(z))

a = 7.9
b = int(a)         # float → int (7.9 → 7)
print(b)
print(type(b))


c = 5
d = float(c)       # int → float (5 → 5.0)
print(d)
print(type(d))
